export function charIndex(ch: string): number {
  const c = ch.charCodeAt(0);
  if (c >= 0x61 && c <= 0x7A) return c - 0x60;       // a-z
  if (c >= 0x41 && c <= 0x5A) return c - 0x40;       // A-Z
  if (c >= 0x30 && c <= 0x39) return c - 0x30 + 27;  // 0-9
  if (c === 0x3A) return 37;                             // colon
  if (c === 0x2F) return 38;                             // forward slash
  if (c === 0x5B) return 39;                             // left bracket
  if (c === 0x5D) return 40;                             // right bracket
  return 0;                                              // period / fallback
}
